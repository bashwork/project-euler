require 'rational'

puts (1..20).inject{ |r,n| r.lcm n }

#----------------------------------------------------------#
# Calclating
#----------------------------------------------------------#
def gcd(a,b)
    while b != 0: 
        a,b = b, a % b
    end
    return a
end

puts (1..20).inject{ |r,n| r * n / gcd(r,n) }
